    static QString getCurrentLanguage() {
        QProcess process;
        process.start("locale");
        if (process.waitForFinished()) {
            QString output = QString::fromUtf8(process.readAllStandardOutput());
            QStringList lines = output.split('\n');
            for (const QString &line : lines) {
                if (line.startsWith("LANG=")) {
                    QString lang = line.mid(5).split('_').first().split('.').first();
                    return lang;
                }
            }
        }
        return "en"; // Default to English
    }

    static QString tr(const QString &key) {
        QString currentLang = getCurrentLanguage();
        
        // Check if we support this language
        if (!translations.contains(currentLang)) {
            currentLang = "en"; // Fallback to English
        }
        
        if (translations[currentLang].contains(key)) {
            return translations[currentLang][key];
        }
        
        // If translation not found, return the key itself
        return key;
    }

